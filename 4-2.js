
function min(a, b) {
    return (a < b) ? a : b;
}

function max(a, b) {
    return (a > b) ? a : b;
}

class Matrix {

    constructor(rows, columns, text) {
        this.rows = rows
        this.columns = columns
        this.matrix = []
        for (let i = 0; i < rows; i++) {
            this.matrix[i] = []
            for (let j = 0; j < columns; j++) {
                this.matrix[i][j] = text[i * columns + j]
            }
        }
    }

    getHorizontalString() {
        let result = ""
        this.matrix.forEach(row => {
            result += row.join("")
            result += " "
        })
        return result
    }

    getVerticalString() {
        let result = ""
        for (let i = 0; i < this.rows; i++) {
            for (let j = 0; j < this.columns; j++) {
                result += this.matrix[j][i]
            }
            result += " "
        }
        return result
    }

    getDiagonalString() {
        let matrix = this.matrix;
        const rows = matrix.length;
        const cols = matrix[0].length;
        let result = '';

        // Traverse the top row (for diagonals starting from the top row)
        for (let startCol = 0; startCol < cols; startCol++) {
            let row = 0, col = startCol;
            while (row < rows && col < cols) {
                result += matrix[row][col];
                row++;
                col++;
            }
            result += " ";
        }

        // Traverse the left column (for diagonals starting from the leftmost column)
        for (let startRow = 1; startRow < rows; startRow++) {
            let row = startRow, col = 0;
            while (row < rows && col < cols) {
                result += matrix[row][col];
                row++;
                col++;
            }
            result += " ";
        }

        return result;
    }

    getAntiDiagonalString() {
        let matrix = this.matrix;
        const ROW = this.rows;
        const COL = this.columns;
        let result = '';
        // There will be ROW+COL-1 lines in the output 
        for (let line = 1;
            line <= (ROW + COL - 1);
            line++) {

            // Get column index of the first 
            // element in this line of output. 
            // The index is 0 for first ROW 
            // lines and line - ROW for remaining lines 
            let start_col = max(0, line - ROW);

            // Get count of elements in this line. 
            // The count of elements is equal to 
            // minimum of line number, COL-start_col and ROW 
            let count = min(line, (COL - start_col),
                ROW);

            // Print elements of this line 
            for (let j = 0; j < count; j++)
                result += (matrix[min(ROW, line) - j - 1][start_col + j]);

            result += " "
        }

        return result;
    }

    traverseIn3by3Chunks(callback) {
        if (this.rows < 3 || this.columns < 3) {
            return
        }

        for (let i = 0; i < this.rows - 2; i++) {
            for (let j = 0; j < this.columns - 2; j++) {
                callback(this.matrix[i][j], this.matrix[i][j + 1], this.matrix[i][j + 2], this.matrix[i + 1][j], this.matrix[i + 1][j + 1], this.matrix[i + 1][j + 2], this.matrix[i + 2][j], this.matrix[i + 2][j + 1], this.matrix[i + 2][j + 2])
            }
        }
    }
}

